#include<iostream>
using namespace std;
int main()
{
    //relational operator

    int a = 10;
    int b = 20;

    cout<< (a<b);
    cout<<"\n";

    cout<<(a>b);
    cout<<"\n";

    cout<<(a!=b);
    cout<<"\n";

    cout<<(a==b);
    cout<<"\n";
    
    //tunary operator

    cout<< ((b>a)?b:a);


}